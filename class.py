class calculator:
    def addition(x, y):
        added = x + y
        print(added)

    def subtraction(x, y):
        sub = x - y
        print(sub)

    def multiplication(x, y):
        mult = x * y
        print(mult)

    def division(x, y):
        div = x / y
        print(div)


c = calculator()

c.addition(1,2)
c.subtraction(4,5)
c.multiplication(1,3)
c.division(4,3)